#include <bits/stdc++.h>
#define ll long long
#define maxn 10010
using namespace std;
int n,t;
ll a[maxn],b[maxn],p[maxn];
int main()
{
  scanf("%d",&t);
  while(t --)
  {
    scanf("%d",&n);
    for(int i = 1;i <= n;i ++)
    {
      scanf("%d",&a[i]);
    }
    for(int i = 1;i <= n;i ++)
    {
      scanf("%d",&b[i]);
    }
    for(int i = 1;i <= n;i ++)
    {
      scanf("%d",&p[i]);
    }
    
  }
}